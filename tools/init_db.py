import sqlite3
import os

# 基于当前文件路径创建数据库，保证和tools同目录
base_dir = os.path.dirname(os.path.abspath(__file__))
db_path = os.path.join(base_dir, 'worldcup.db')

# 连接数据库（自动创建 worldcup.db）
conn = sqlite3.connect(db_path)
cursor = conn.cursor()

# 每次运行脚本都从干净数据库重建，避免重复插入导致赛程、球员和进球事件翻倍。
cursor.executescript('''
DROP TABLE IF EXISTS match_details;
DROP TABLE IF EXISTS goalkeepers;
DROP TABLE IF EXISTS players;
DROP TABLE IF EXISTS matches;
''')

# ========== 1. 创建赛程表 matches ==========
cursor.execute('''
CREATE TABLE IF NOT EXISTS matches (
    match_id INTEGER PRIMARY KEY AUTOINCREMENT,
    match_date TEXT NOT NULL,
    match_time TEXT NOT NULL,
    home_team TEXT NOT NULL,
    away_team TEXT NOT NULL,
    stage TEXT NOT NULL,
    home_score INTEGER,
    away_score INTEGER
)
''')

# ========== 2. 创建比赛详情表 match_details ==========
cursor.execute('''
CREATE TABLE IF NOT EXISTS match_details (
    detail_id INTEGER PRIMARY KEY AUTOINCREMENT,
    match_id INTEGER NOT NULL,
    player_name TEXT NOT NULL,
    team TEXT NOT NULL,
    goal_time INTEGER NOT NULL,
    event_type TEXT DEFAULT 'goal'
)
''')

# ========== 3. 创建场上球员统计表 players（含出场时间、红黄牌） ==========
# 字段：姓名、队伍、进球、助攻、出场场次、总出场分钟、场均分钟、红牌、黄牌
cursor.execute('''
CREATE TABLE IF NOT EXISTS players (
    player_id INTEGER PRIMARY KEY AUTOINCREMENT,
    player_name TEXT NOT NULL,
    team TEXT NOT NULL,
    goals INTEGER DEFAULT 0,
    assists INTEGER DEFAULT 0,
    appearances INTEGER DEFAULT 0,
    total_minutes INTEGER DEFAULT 0,
    avg_minutes REAL DEFAULT 0.0,
    red_cards INTEGER DEFAULT 0,
    yellow_cards INTEGER DEFAULT 0
)
''')

# ========== 4. 创建守门员专用表 goalkeepers（扑救、扑救成功率） ==========
cursor.execute('''
CREATE TABLE IF NOT EXISTS goalkeepers (
    gk_id INTEGER PRIMARY KEY AUTOINCREMENT,
    player_name TEXT NOT NULL,
    team TEXT NOT NULL,
    saves INTEGER DEFAULT 0,
    save_rate REAL DEFAULT 0.0
)
''')

